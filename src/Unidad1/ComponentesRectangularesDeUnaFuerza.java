    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        double fq, fp, aq, ap, qx, qy, px, py, rx, ry, fr, ar;
        
        String operaciones = "";
        fp = Double.parseDouble(jTextField1.getText());
        ap = Double.parseDouble(jTextField4.getText());
        
        fq = Double.parseDouble(jTextField2.getText());
        aq = Double.parseDouble(jTextField5.getText());
        
        
        qx = fq*Math.cos(Math.toRadians(aq));
        operaciones = " Qx = " + fq + "* Cos " + aq + " = " + qx; 
 
        qy = fq*Math.sin(Math.toRadians(aq));
        operaciones = operaciones + "\n \n Qy = " + fq + "* Sin " + aq + " = " + qy; 
        
        px = fp*Math.cos(Math.toRadians(ap));
        operaciones = operaciones + "\n \n Px = " + fq + "* Cos " + ap + " = " + px;
        
        py = fp*Math.sin(Math.toRadians(ap));
        operaciones = operaciones + "\n \n Py = " + fq + "* Sin " + ap + " = " + py;
        
        rx = px + qx;
        operaciones = operaciones + "\n \n FRx = " + rx;
        
        ry = py + qy;
        operaciones = operaciones + "\n \n FRy = " + ry;
        
        fr = Math.sqrt(Math.pow(rx,2) + Math.pow(ry,2));
        
        ar = Math.tanh(Math.toRadians(ry/rx));
        
        jLabel8.setText("" + fr);
        jLabel9.setText("" + ar);
        
        jTextArea1.setText(operaciones);
    }//GEN-LAST:event_jButton1ActionPerformed

